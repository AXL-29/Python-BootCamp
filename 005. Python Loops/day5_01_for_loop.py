# for loop - is used to repeat a blocked of code multiple times, usually to go through (or iterate over)
# a sequence like a list, string, tuple, dictionary, or range of numbers.

# syntax:
# for variable in sequence:
#   do something.

# variable - takes each value from the sequence one by one.
# sequence - can be list, string, range, etc.

fruits = ["apple", "mango", "pear", "orange", "banana"]

for fruit in fruits:
    print(fruit)