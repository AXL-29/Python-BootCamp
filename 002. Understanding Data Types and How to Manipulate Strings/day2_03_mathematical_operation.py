# PEMDASLR - Parethheses, Exponents, Multiplication/Division, and Addition/Subtraction (Left - Right).

print("My age: " + str(25))
print(123 + 456)    # addition
print(7 - 4)        # subtraction
print(12 * 5)       # multiplication
print(15 / 4)       # division
print(15 // 4)      # floor division
print(15 % 4)       # modulus
print(3 ** 4)       # exponential

