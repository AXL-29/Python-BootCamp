# In Python, a global constant is a variable defined at the top level of your file (global scope)
# that you don't intend to change.

# Python doesn't have true constant built into the language, so we follow a naming convention to say "please treat this as a constant".

# How we usually define a global constant:
#     - Written in UPPER_SNAKE_CASE
#     - Defined outside any function or class

