# API authentication is the process of proving who you are when your 
# program communicates with another system through an API

# Without Authentication:
    # Anyone could access the API 
    # Data could be stolen or abused
    # Services could be misused or overcharged

# Why API authentication is important:
    # Security - protects data
    # Usage tracking - rate limits & billing
    # Access control - who can do what
    # Audit logs - trace actions

# API Key is a unique secret string that identifies whos is making an API request.
# API key = ID card for your application.

