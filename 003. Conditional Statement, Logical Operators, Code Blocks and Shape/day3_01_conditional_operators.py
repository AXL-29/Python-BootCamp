# Control Flow - decides which part of your code runs based on conditions (like "if this happens, do that").
# Python uses keywords like if, elif, and else for decision-making.

# Syntax of if-else:
# if condition:
#     do this
# else:
#     do this

# Indentation - means adding spaces before a line of code to show that it belongs to a group or block of code.

print("Welcome to the rollercoaster!")
height = float(input("What is your height in cm?: "))

if height > 120:
    print("You can ride the rollercoaster!")
else:
    print("Sorry, you can't ride the rollercoaster.")

# IndentationError: expected an indented block
# This means Python expected some code to be indented, but it wasn't.

